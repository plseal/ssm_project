
package com.lin.entity;


public class FixedEntity
{

	private String id;
	private String name;
	private String used;

	public FixedEntity()
	{
	}

	public String getId()
	{
		return id;
	}

	public void setId(String id)
	{
		this.id = id;
	}

	public String getName()
	{
		return name;
	}

	public void setName(String name)
	{
		this.name = name;
	}

	public String getUsed()
	{
		return used;
	}

	public void setUsed(String used)
	{
		this.used = used;
	}
}
