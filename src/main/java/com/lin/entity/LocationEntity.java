

package com.lin.entity;


public class LocationEntity
{

	private String id;
	
	private String name;
	

	public LocationEntity()
	{
	}

	public String getId()
	{
		return id;
	}

	public void setId(String id)
	{
		this.id = id;
	}

	public String getName()
	{
		return name;
	}

	public void setName(String name)
	{
		this.name = name;
	}

}
